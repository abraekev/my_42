#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	const char	*tmp;

	if (!s)
		return (NULL);
	tmp = NULL;
	while (*s)
	{
		if (*s == (char)c)
			tmp = s;
		s++;
	}
	if (!c)
		return ((char *)s);
	return ((char *)tmp);
}
